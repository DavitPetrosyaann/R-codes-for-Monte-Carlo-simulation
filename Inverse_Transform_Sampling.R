# Պարամետր
lambda <- 2
# Uniform պատահական թիվ [0, 1] միջակայքում
u <- runif(1)
# Հակադարձ կուտակային բաշխման միջոցով նմուշ
x <- -log(1 - u) / lambda
print(x)

# Քանի որ (1-u)~Uniform ապա գոյություն ունի կոդի ավելի կարճ տարբերակ 
x <- -log(runif(1)) / lambda
print(x)


# Այս կոդը գեներացնում է մեկ պատահական արժեք էքսպոնենցիալ բաշխումից՝ λ=2 պարամետրով։